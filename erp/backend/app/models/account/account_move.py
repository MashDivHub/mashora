    auto_post_until: Mapped[Optional[date]] = mapped_column(Date, nullable=True)
    invoice_date: Mapped[Optional[date]] = mapped_column(Date, nullable=True)
    invoice_date_due: Mapped[Optional[date]] = mapped_column(Date, nullable=True)
    narration: Mapped[Optional[dict]] = mapped_column(JSONB, nullable=True)
    amount_untaxed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_tax: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_total: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_residual: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_untaxed_signed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_tax_signed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_total_signed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_total_in_currency_signed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
    amount_residual_signed: Mapped[Optional[float]] = mapped_column(Numeric, nullable=True)
