    tax_cash_basis_rec_id: Mapped[Optional[int]] = mapped_column(Integer, nullable=True)
    tax_cash_basis_origin_move_id: Mapped[Optional[int]] = mapped_column(Integer, nullable=True)
    auto_post_origin_id: Mapped[Optional[int]] = mapped_column(Integer, nullable=True)
    sequence_prefix: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    access_token: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    name: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    ref: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    state: Mapped[str] = mapped_column(String, nullable=False, server_default="draft")
    move_type: Mapped[str] = mapped_column(String, nullable=False)
    auto_post: Mapped[str] = mapped_column(String, nullable=False, server_default="no")
    inalterable_hash: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    payment_reference: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    qr_code_method: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    payment_state: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    invoice_source_email: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    invoice_partner_display_name: Mapped[Optional[str]] = mapped_column(String, nullable=True)
    invoice_origin: Mapped[Optional[str]] = mapped_column(String, nullable=True)
